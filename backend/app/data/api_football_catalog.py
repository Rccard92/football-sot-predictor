def _v04_manifest_framework_keys() -> frozenset[str]:
    mv = BASELINE_SOT_MODEL_VERSION_V04_OFFENSIVE_CORE_SOT
    return frozenset(s.framework_key for s in manifest_for_model(mv) if s.framework_key)


def _enrich_parameter(row: dict[str, Any], v04_fw: frozenset[str]) -> dict[str, Any]:
    out = deepcopy(row)
    ck = str(out["key"])
    fw_list = list(CATALOG_KEY_TO_FRAMEWORK_KEYS.get(ck, []))
    out["framework_keys"] = fw_list
    out["in_v04_manifest"] = any(k in v04_fw for k in fw_list)
    return out


def build_api_football_catalog_payload() -> dict[str, Any]:
    """Costruisce il payload JSON completo (read-only)."""
    v04_fw = _v04_manifest_framework_keys()
    rows = [_enrich_parameter(r, v04_fw) for r in _RAW_PARAMETERS]

    by_area: dict[str, list[dict[str, Any]]] = {a["id"]: [] for a in _AREA_DEFS}
    for r in rows:
        aid = str(r["area_id"])
        by_area.setdefault(aid, []).append(r)

    areas_out: list[dict[str, Any]] = []
    for a in _AREA_DEFS:
        aid = a["id"]
        params = by_area.get(aid, [])
        areas_out.append(
            {
                "id": aid,
                "title": a["title"],
                "description_it": a["description_it"],
                "parameters": params,
            }
        )

    return {
        "version": CATALOG_VERSION,
        "provider": CATALOG_PROVIDER,
        "model_version_reference": BASELINE_SOT_MODEL_VERSION_V04_OFFENSIVE_CORE_SOT,
        "areas": areas_out,
    }


def count_catalog_parameters() -> int:
    return len(_RAW_PARAMETERS)
